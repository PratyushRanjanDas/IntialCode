
public class string 
{
    public static void main(String[] args) 
    {
        String boi = " Chinu ";
        System.out.println(boi.toLowerCase());
        System.out.println(boi.toUpperCase());
        System.out.println(boi.trim());
        System.out.println(boi.substring(4));
        System.out.println(boi.substring(4,7));
        System.out.println(boi.replace('u','C'));
        System.out.println(boi.replace("Chi","Tar"));
        System.out.println(boi.startsWith(" C"));
        System.out.println(boi.endsWith("u "));
    }

}
